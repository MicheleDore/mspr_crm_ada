
-- Création de la base de données (si nécessaire)
CREATE DATABASE IF NOT EXISTS Customer_Relationship_Management;
USE Customer_Relationship_Management;

-- Création de la table Personnes
CREATE TABLE IF NOT EXISTS Personnes(
    ID_Personne INT(11) AUTO_INCREMENT PRIMARY KEY,
    Nom VARCHAR(50) NOT NULL,
    Prenom VARCHAR(50) NOT NULL,
    CP VARCHAR(20) NOT NULL,
    Ville VARCHAR(50) NOT NULL,
    Rue VARCHAR(100) NOT NULL,
    TelephonePerso VARCHAR(20) NOT NULL,
    MailPerso VARCHAR(100) NOT NULL
);

-- Création de la table Clients
CREATE TABLE IF NOT EXISTS Clients(
    ID_Client INT(11),
    Date_Enregistrement DATE NOT NULL
);

-- Création de la table Employes
CREATE TABLE IF NOT EXISTS Employes(
    ID_Employe INT(11),
    Matricule INT(11) NOT NULL,
    DateEmbauche DATE NOT NULL,
    MailPro VARCHAR(100) NOT NULL
);

-- Création de la table Commandes
CREATE TABLE IF NOT EXISTS Commandes(
    ID_Commande INT(11) AUTO_INCREMENT PRIMARY KEY,
    DateCommande DATE NOT NULL,
    NumeroCommande INT(11),
    ID_Client INT(11),
    ID_Employe INT(11)
);

-- Création de la table ContenuCommandes
CREATE TABLE IF NOT EXISTS ContenuCommandes(
    ID_Commande INT(11),
    ID_Produit INT(11),
    Quantite INT(11) NOT NULL
);

-- Création de la table Produits
CREATE TABLE IF NOT EXISTS Produits(
    ID_Produit INT(11) AUTO_INCREMENT PRIMARY KEY,
    NomProduit VARCHAR(100) NOT NULL,
    Prix DECIMAL(11,2) NOT NULL,
    Description VARCHAR(250),
    DateCreationProduit DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS HistoriqueProduit(
    ID INT(11) AUTO_INCREMENT PRIMARY KEY,
    DateArchivageProduit DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS HistoriqueCommande(
    ID INT(11) AUTO_INCREMENT PRIMARY KEY,
    DateCommande DATE NOT NULL,
    NumeroCommande INT(11) NOT NULL
);



ALTER TABLE Clients
ADD FOREIGN KEY (ID_Client) REFERENCES Personnes(ID_Personne);

ALTER TABLE Employes
ADD FOREIGN KEY (ID_Employe) REFERENCES Personnes(ID_Personne);

ALTER TABLE Commandes
ADD FOREIGN KEY (ID_Client) REFERENCES Clients(ID_Client),
ADD FOREIGN KEY (ID_Employe) REFERENCES Employes(ID_Employe);

ALTER TABLE ContenuCommandes
ADD FOREIGN KEY (ID_Commande) REFERENCES Commandes(ID_Commande),
ADD FOREIGN KEY (ID_Produit) REFERENCES Produits(ID_Produit);

